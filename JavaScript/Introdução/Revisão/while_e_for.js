/*
    while -> ENQUANTO
    for -> PARA
*/

// Exemplo 1 
let x = 1

while(x <= 10) {
    console.log(x)
    x = x + 1
}

// Exemplo 2 - macete

while(x <= 10) {
    console.log(x)
    x++
}

// Exemplo 1

for ( x=1 ; x<=10 ; x++ ){
    console.log(x)
}

// Exemplo 2

for ( x=10 ; x>=1 ; x-- ){
    console.log(x)
}